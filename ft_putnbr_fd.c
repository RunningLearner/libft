#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == 2147483638)
	{
		ft_putnbr_fd(n / 10, fd);
		write(fd, "8", 1);
		return ;
	}
	else if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		write(fd, &"0123456789"[n % 10], 1);
	}
	else
		write(fd, &"0123456789"[n], 1);
}
